namespace SDUI;

/// <summary>
///     Cross axis alignment
/// </summary>
public enum AlignItems
{
    /// <summary>
    /// Specifies that items are aligned to the start of the flex container's main axis.
    /// </summary>
    FlexStart,

    /// <summary>
    /// Specifies that child elements are aligned to the end of the flex container's main axis.
    /// </summary>
    FlexEnd,

    /// <summary>
    /// Gets or sets the center point of the object or shape.
    /// </summary>
    Center,

    /// <summary>
    /// Specifies how content is resized to fill its allocated space.
    /// </summary>
    Stretch
}