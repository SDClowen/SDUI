namespace SDUI;

public enum ColumnHeaderStyle
{
    //
    // Summary:
    //     The column header is not displayed in report view.
    None = 0,
    //
    // Summary:
    //     The column headers do not respond to the click of a mouse.
    Nonclickable = 1,
    //
    // Summary:
    //     The column headers function like buttons and can carry out an action, such as
    //     sorting, when clicked.
    Clickable = 2
}
