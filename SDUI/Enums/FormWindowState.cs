namespace SDUI;

public enum FormWindowState
{
    /// <summary>
    /// Represents the normal log level, typically used for informational messages that highlight the progress of the
    /// application at a coarse-grained level.
    /// </summary>
    Normal,

    /// <summary>
    /// Indicates whether the window is minimized.
    /// </summary>
    Minimized,

    /// <summary>
    /// Indicates that the window is maximized.
    /// </summary>
    Maximized
}
