namespace SDUI;


/// <summary>
/// Flex container direction
/// </summary>
public enum FlexDirection
{
    /// <summary>
    /// Represents a single row of data within a collection or table structure.
    /// </summary>
    Row,

    /// <summary>
    /// Specifies that items are arranged in rows in reverse order.
    /// </summary>
    RowReverse,

    /// <summary>
    /// Represents a single column in a data structure, such as a table or grid.
    /// </summary>
    Column,

    /// <summary>
    /// Specifies that the order of items in a column is reversed.
    /// </summary>
    ColumnReverse
}
